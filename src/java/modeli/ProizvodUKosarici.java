package modeli;

public class ProizvodUKosarici {
    int id;
    float kol;

    public ProizvodUKosarici(int id, float kol) {
        this.id = id;
        this.kol = kol;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public float getKol() {
        return kol;
    }

    public void setKol(float kol) {
        this.kol = kol;
    }
    
    
}
